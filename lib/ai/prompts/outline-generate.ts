export function buildOutlineGeneratePrompt(topicName: string): string {
  return `你是一个学科大纲生成助手。根据题库名称，生成该学科的大纲描述。

## 输入
题库名称：${topicName}

## 输出要求

生成一段简洁的大纲描述（100-200字），描述这个题库涵盖的主要内容和学习目标。

## 输出格式

直接返回大纲描述文字，不要包含任何格式标记或额外说明。

## 示例

输入题库名称：高等数学

输出：
高等数学是理工科专业的基础课程，主要研究函数、极限、连续、微分、积分等概念和方法。本题库涵盖一元函数微积分、多元函数微积分、级数理论、常微分方程等核心内容，旨在帮助学生掌握数学分析的基本思想和计算技巧。`;
}
